using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace DeclareVariables
{
    class DeclareVariable
    {
        static void Main(string[] args)
        {
            ushort firstVar = 52130;
            sbyte secondVar = -115;
            int thirdVar = 4825932;
            byte fourthVar = 97;
            short fifthVar = -10000;
            Console.WriteLine("{0} \n {1} \n {2} \n {3} \n {4}", firstVar, secondVar, thirdVar, fourthVar, fifthVar);
        }
    }
}
